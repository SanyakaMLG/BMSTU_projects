package com.company;

public class Vector {
    private double x, y, z;
    public Vector (double inX, double inY, double inZ) {
        this.x = inX;
        this.y = inY;
        this.z = inZ;
    }
    public double getX() {
        return this.x;
    }
    public double getY() {
        return this.x;
    }
    public double getZ() {
        return this.z;
    }
}
